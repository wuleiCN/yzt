import request from '@/utils/request'

export function getList(data) {
  return request({
    url: '/productPurchase/list',
    method: 'post',
    data
  })
}
export function saveOrUpdate(data) {
  return request({
    url: '/productPurchase/saveOrUpdate',
    method: 'post',
    data
  })
}
export function del(data) {
  return request({
    url: '/productPurchase/delete',
    method: 'post',
    data
  })
}
export function productSelectList(data) {
  return request({
    url: '/product/optionList',
    method: 'post',
    data
  })
}

