import request from '@/utils/request'

export function getList(data) {
  return request({
    url: '/weekly/list',
    method: 'post',
    data
  })
}
export function saveOrUpdate(data) {
  return request({
    url: '/weekly/saveOrUpdate',
    method: 'post',
    data
  })
}

export function del(data) {
  return request({
    url: '/weekly/delete',
    method: 'post',
    data
  })
}

