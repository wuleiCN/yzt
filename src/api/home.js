import request from '@/utils/request'

export function getList(data) {
  return request({
    url: '/projects/indexProInfo',
    method: 'post',
    data
  })
}

export function detail(data) {
  return request({
    url: '/projects/detail',
    method: 'post',
    data
  })
}
export function getStatistics(data) {
  return request({
    url: '/projects/statistics',
    method: 'post',
    data
  })
}

