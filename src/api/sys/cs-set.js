import request from '@/utils/request'

export function getList(data) {
  return request({
    url: '/xzresources/list',
    method: 'post',
    data
  })
}

export function saveOrUpdate(data) {
  return request({
    url: '/xzresources/saveOrUpdate',
    method: 'post',
    data
  })
}

export function del(data) {
  return request({
    url: '/xzresources/delete',
    method: 'post',
    data
  })
}
