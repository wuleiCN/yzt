import request from '@/utils/request-zhgd'

export function getList(data) {
  return request({
    url: '/dictData/findDictDataList',
    method: 'post',
    data
  })
}
export function getTypeList(data) {
  return request({
    url: '/dictType/findTypeList',
    method: 'get',
    data
  })
}

export function save(data) {
  return request({
    url: '/dictData/saveDictData',
    method: 'post',
    data
  })
}
export function update(data) {
  return request({
    url: '/dictData/updateDictDataById',
    method: 'post',
    data
  })
}

export function del(params) {
  return request({
    url: '/dictData/deleteDictDataById',
    method: 'get',
    params
  })
}
