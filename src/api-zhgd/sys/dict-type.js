import request from '@/utils/request-zhgd'

export function getList(data) {
  return request({
    url: '/dictType/findDictTypeList',
    method: 'post',
    data
  })
}

export function save(data) {
  return request({
    url: '/dictType/saveDictType',
    method: 'post',
    data
  })
}
export function update(data) {
  return request({
    url: '/dictType/updateDictTypeById',
    method: 'post',
    data
  })
}

export function del(params) {
  return request({
    url: '/dictType/deleteDictTypeById',
    method: 'get',
    params
  })
}
